export function add(x,y){
  return x+y;
}

export function sub(x,y){
  return x-y;
}

export function div(x,y){
  return x/y;
}

export function mux(x,y){
  return x*y;
}

export function pow(x,a) {
  /*
  * x to the a power
  */
  return Math.pow(x,a)
}

export function mod(x,a) {
  /*
  * x mod a
  */
  return x%a;
}